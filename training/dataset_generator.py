def clean_output(text):
    # Remove repeated question headers
    text = re.sub(r'\*\*Question:.*?\*\*\n', '', text)
    text = re.sub(r'---\n', '', text)

    # Trim to 500 words max
    words = text.split()
    if len(words) > 500:
        text = ' '.join(words[:500]) + "..."

    return text.strip()

